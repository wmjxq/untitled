package edu.xcdq;

import java.util.Locale;

public class demo03 {
    public static void main(String[] args) {
        //  4 toUpperCase  转为大写
        //  5 toLowerCase  转为小写
        String s = "Tom";
        System.out.println(s.toLowerCase());   //tom
        System.out.println(s.toUpperCase());  //TOM
    }
}
